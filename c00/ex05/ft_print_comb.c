#include <unistd.h>
void	ft_print_comb(void)
{
	int		i;
	char	c;

	i = 12;
	write (1, "012", 3);
	while (i < 789)
	{
		i++;
		if (i / 100 < i % 100 / 10 & i % 100 / 10 < i % 10)
		{
			write (1, ", ", 2);
			c = i / 100 + '0';
			write (1, &c, 1);
			c = i % 100 / 10 + '0';
			write (1, &c, 1);
			c = i % 10 + '0';
			write (1, &c, 1);
		}
	}
	write (1, "\n", 1);
}
